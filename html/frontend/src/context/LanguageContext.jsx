export function LanguageProvider({ children }) {
  const normalizeLang = (lang) => {
    if (!lang) return 'ru';
    const l = String(lang).toLowerCase();
    if (l.startsWith('ru')) return 'ru';
    if (l.startsWith('ro')) return 'ro';
    return 'ru';
  };
  const [language, setLanguage] = useState(() => {
    return normalizeLang(localStorage.getItem('language')) || 'ru';
  });

  // Функция синхронизации с бэкендом
  async function syncLanguageWithBackend(lang) {
    try {
      const token = localStorage.getItem('token');
      if (!token) return;
      
      // Use client.js logic manually since we can't import axios easily inside context if cyclic
      const response = await fetch('/api/v1/auth/me/language', {
        method: 'PUT',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${token}`
        },
        body: JSON.stringify({ language: lang })
      });
      
      if (response.ok) {
        console.log('✅ Language synced with backend:', lang);
      }
    } catch (error) {
      console.error('❌ Failed to sync language:', error);
    }
  }

  useEffect(() => {
    localStorage.setItem('language', language);
    document.documentElement.lang = language;
    
    // Синхронизируем с бэкендом при входе
    const token = localStorage.getItem('token');
    if (token) {
      syncLanguageWithBackend(language);
    }
  }, [language]);
  
  // Слушаем изменения языка из других компонентов
  useEffect(() => {
    const handleLanguageChange = () => {
      const newLang = localStorage.getItem('language');
      if (newLang && newLang !== language) {
        setLanguage(newLang);
      }
    };
    
    window.addEventListener('languageChange', handleLanguageChange);
    return () => window.removeEventListener('languageChange', handleLanguageChange);
  }, [language]);
  
  const t = (key) => {
    const lang = normalizeLang(language);
    const valPrimary = translations[lang]?.[key];
    if (valPrimary !== undefined) return valPrimary;
    const valRu = translations['ru']?.[key];
    if (valRu !== undefined) return valRu;
    const valRo = translations['ro']?.[key];
    if (valRo !== undefined) return valRo;
    return key;
  };

  const changeLanguage = (lang) => {
    setLanguage(normalizeLang(lang));
  };

  const contextValue = {
    language,
    changeLanguage,
    t
  };

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  // Debug log
  // console.log('useLanguage called, LanguageContext:', LanguageContext);
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return context;
}


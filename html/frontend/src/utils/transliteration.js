// Removed unused latinToCyrillicMap to satisfy lint

export const transliterate = (text, targetLang) => {
  if (!text) return '';
  const str = String(text); // Ensure string
  
  // If target is Romanian (or English/Latin based), convert Cyrillic to Latin
  if (targetLang === 'ro' || targetLang === 'en') {
    return str.split('').map(char => cyrillicToLatinMap[char] || char).join('');
  }
  
  return str;
};

export const getLocalizedName = (firstName, lastName, language) => {
  const first = transliterate(firstName, language);
  const last = transliterate(lastName, language);
  return `${first} ${last}`.trim();
};
